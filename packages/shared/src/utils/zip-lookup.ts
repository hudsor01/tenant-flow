/**
 * Normalize ZIP code to 5-digit format
 * @param zip Raw ZIP code input (may include dash and +4)
 * @returns 5-digit ZIP code or null if invalid
 */
export function normalizeZipCode(zip: string): string | null {
	if (!zip) return null
	
	// Remove all non-digits
	const digits = zip.replace(/\D/g, '')
	
	// Extract first 5 digits
	if (digits.length < 5) return null
	
	return digits.substring(0, 5)
}

/**
 * Lookup city and state from ZIP code
 * @param zip ZIP code (5 digits, optionally with dash and +4)
 * @returns City and state data, or null if not found
 * 
 * @example
 * lookupZipCode('90210') // { city: 'Beverly Hills', state: 'CA' }
 * lookupZipCode('90210-1234') // { city: 'Beverly Hills', state: 'CA' }
 * lookupZipCode('00000') // null
 */
export function lookupZipCode(zip: string): ZipData | null {
	const normalized = normalizeZipCode(zip)
	if (!normalized) return null
	
	return zipLookup[normalized] || null
}

/**
 * Check if a ZIP code is in the lookup table
 * @param zip ZIP code to check
 * @returns true if ZIP code is recognized
 */
export function isZipCodeKnown(zip: string): boolean {
	const normalized = normalizeZipCode(zip)
	return normalized !== null && normalized in zipLookup
}

/**
 * Get all known ZIP codes
 * @returns Array of all ZIP codes in the lookup table
 */
export function getAllZipCodes(): string[] {
	return Object.keys(zipLookup)
}

/**
 * Get statistics about the ZIP code database
 * @returns Database statistics
 */
export function getZipLookupStats() {
	const zips = Object.keys(zipLookup)
	const states = new Set(Object.values(zipLookup).map(d => d.state))
	const cities = new Set(Object.values(zipLookup).map(d => d.city))
	
	return {
		totalZipCodes: zips.length,
		uniqueStates: states.size,
		uniqueCities: cities.size,
		coverage: 'Top 500 US ZIP codes (~40% of population)'
	}
}
